// ── Helpers ──────────────────────────────────────────────────

export function getSeller(id: string): Seller | undefined {
  return sellers.find(s => s.id === id);
}

export function getListingsByCategory(category: Category): Listing[] {
  return listings.filter(l => l.category === category);
}

export function getListingBySlug(slug: string): Listing | undefined {
  return listings.find(l => l.slug === slug);
}

export function getFeaturedListings(limit = 12): Listing[] {
  return listings.filter(l => l.featured).slice(0, limit);
}

export function getRecentListings(limit = 8): Listing[] {
  return [...listings]
    .sort((a, b) => new Date(b.timestamp).getTime() - new Date(a.timestamp).getTime())
    .slice(0, limit);
}

export function getSimilarListings(listing: Listing, limit = 4): Listing[] {
  return listings
    .filter(l => l.category === listing.category && l.id !== listing.id)
    .slice(0, limit);
}

export function getSellerListings(sellerId: string, excludeSlug?: string): Listing[] {
  return listings.filter(l => l.sellerId === sellerId && l.slug !== excludeSlug);
}

export function formatPrice(price: number): string {
  return '€' + price.toLocaleString('en-IE');
}

export function getCategoryMeta(slug: string): CategoryMeta | undefined {
  return categories.find(c => c.slug === slug);
}

export function getCategoryCounts(): Record<Category, number> {
  const counts = {} as Record<Category, number>;
  categories.forEach(c => { counts[c.slug] = 0; });
  listings.forEach(l => { counts[l.category]++; });
  return counts;
}
